#include "libft.h"

int		ft_size_number(int n)
{
	int		len;

	len = 0;
	if (n < 0)
	{
		len = 1;
		n = -n;
	}
	while (n != 0)
	{
		n /= 10;
		len++;
	}
	return (len);
}
